package com.nichi.model;

/**
 * @author nichiuser
 *
 */
public class ButtonSpecifications {

	private int left = 0;
	private int top = 0;
	private int height = 0;
	private int width = 0;
	public int getLeft() {
		return left;
	}
	public void setLeft(int left) {
		this.left = left;
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}

	
	
}
